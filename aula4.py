"""
tipos int e float

int -> Representa qualquer numero inteiro positivo ou negativo

float -> representa qualquer numero com ponto flutuante, ou seja, numeros racionais

## class "type" mostra o tipo de dado que o python inferiou o valor
"""


x = 2
print(type(x))
# Como não foi passado o tipo de dado para x, o python infere que o valor é um int

y = 2.5
print(type(y))
# Como não foi passado o tipo de dado para x, o python infere que o valor é um float
