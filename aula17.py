# Utilizando condicionais if / elif / else

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = False

if condicao1:
    print('Código condição 1')
elif condicao2:
    print('Código condição 2')
elif condicao3:
    print('Código condição 3')
elif condicao4:
    print('Código condição 4')
else:
    print('Nenhuma condição satisfeita')